              /></span>
              Operational limitations may have been encountered during the
              inspection such as inability to gain access to parts of the
              installation or to an item of equipment. The inspector should have
              noted any such limitations in PART 7. It should be noted that the
              greater the limitations applying to a report, the less its value
              from the safety aspect.<br />
              A declaration should have been given by the inspector in PART 4 of
              the report. The declaration must reflect the statement given in
              PART 3, which summarises the observations and recommendations made
              in PART 6. Where one or more observations have been made in PART
              6, the Classification code given to each by the inspector
              indicates the degree of urgency with which remedial action needs
              to be taken to restore the installation to a safe working
              condition.<br />
              Where the inspector has indicated an observation as code C1
              (danger present)
              <span style="font-weight: 900"
                >the safety of those using the installation is at risk.</span
              >
              Wherever practicable, items classified as (C1) should be made safe
              on discovery, and it is recommended that a skilled person(s)
              competent in electrical installation work undertakes the necessary
              remedial work immediately.<br />
              Where the inspector has indicated an observation as code C2
              (potentially dangerous)
              <span style="font-weight: 900"
                >the safety of those using the installation may be at
                risk,</span
              >
              and it is recommended that a skilled person(s) competent in
              electrical installation work undertakes the necessary remedial
              work as a matter of urgency.<br />
              Where the inspector has indicated that an item requires further
              investigation (FI), the investigation should be carried out
              without delay to determine whether danger or potential danger
              exists. For further guidance on the Classification codes, please
              see the reverse of page 2.<br />
              Where the installation can be supplied by more than one source,
              such as the public supply and a standby generator or
              microgenerator, this should be identified in PART 8 Supply
              Characteristics and Earthing Arrangements, and the Schedules of
              Circuit Details and Test Results (PART 12) compiled
              accordingly.<br />
              Where inadequacies in the intake equipment have been observed
              (Item 1 of PART 10), the person ordering the inspection should
              inform the distributor and/or supplier as appropriate.<br />
              Should the person ordering this report have reason to believe that
              it does not reasonably reflect the condition of the electrical
              installation reported on, that person should in the first instance
              raise the specific concerns in writing with the contractor.
