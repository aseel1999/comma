      <h2 style="text-align: center; margin: 0">
        GUIDANCE FOR RECIPIENTS ON THE CLASSIFICATION CODES
      </h2>
      <h4
        style="
          text-align: center;
          margin: 0;
          font-weight: 700;
          text-decoration: underline;
        "
      >
        Only one Classification code should be given for each recorded
        Observation
      </h4>
      <table style="padding: 20px; margin-bottom: 180px;">
        <tr>
          <th style="padding: 0 15px 0 0">
            <p style="text-align: start; font-size: 12px; margin: 0 0 15px 0">
              <span style="font-weight: 900"
                >Classification code C1 (Danger present)<br
              /></span>
              Where an observation has been given a Classification code C1, the
              safety of those using the installation is at risk and immediate
              remedial action is required.<br />
              The person responsible for the maintenance of the installation is
              advised to take action without delay to remedy the observed
              deficiency in the installation, or to take other appropriate
              action (such as switching off and isolating the affected part(s)
              of the installation) to remove the danger. The contractor issuing
              this report will be able to provide further advice.<br />
              NICEIC makes available ‘Electrical Danger Notification’ forms to
              enable inspectors to record, and then to communicate to the person
              ordering the report, any dangerous condition discovered.
            </p>
            <p style="text-align: start; font-size: 12px; margin: 0 0 15px 0">
              <span style="font-weight: 900"
                >Classification code C2 (Potentially dangerous)<br
              /></span>
              Classification code C2 indicates that, whilst those using the
              installation may not be at immediate risk, urgent remedial action
              is required to remove potential danger. The contractor issuing
              this report will be able to provide further advice.<br />
              It is important to note that the recommendation given at PART 5 of
              this report (Next Inspection) for the maximum interval until the
              next inspection is conditional upon all items which have been
              given a Classification code C1 and code C2 being remedied
              immediately and as a matter of urgency, respectively. It would not
              be reasonable for the inspector to indicate that the installation
              is in a satisfactory condition if any observation in this report
              has been given a code C1 or code C2 classification.
            </p>
            <p style="text-align: start; font-size: 12px; margin: 0 0 15px 0">
              <span style="font-weight: 900"
                >Classification code C3 (Improvement recommended)<br
              /></span>
              Where an observation has been given a Classification code C3, the
              inspection and/or testing has revealed a non-compliance with the
              current safety standard which, whilst not presenting immediate or
              potential danger, would result in a significant safety improvement
              if remedied. Careful consideration should be given to the safety
              benefits of improving these aspects of the installation. The
              contractor issuing this report will be able to provide further
              advice.
            </p>
          </th>
          <th style="padding: 0 15px 60px 0">
            <p style="text-align: start; font-size: 12px; margin: 0 0 15px 0">
              <span style="font-weight: 900"
                >Code FI (Further investigation required without delay) <br
              /></span>
              It should usually be possible for the inspector to attribute a
              Classification code to each observation without indicating a need
              for further investigation.<br />
              However, where ‘FI’ has been entered against an observation the
              inspector considers that further investigation of that observation
              is likely to reveal danger or potential danger that, due to the
              agreed extent or limitations of the inspection and/or testing,
              could not be fully identified at the time.<br />
              It would not be appropriate for the inspector to indicate that the
              installation is in a satisfactory condition if there is reasonable
              doubt as to whether danger or potential danger exists.
              Consequently, where the inspector has indicated ‘Further
              investigation required without delay’ (FI) the overall assessment
              of the installation (PART 3) should be marked as
              ‘Unsatisfactory’.<br />
              If the inspector has indicated that an observation requires
              further investigation without delay, the person ordering this
              report is advised to arrange for the contractor issuing the report
              (or another skilled person or persons competent in such work) to
              undertake further examination of that aspect of the installation
              as a matter of urgency, to determine whether or not danger or
              potential danger exists.
            </p>
            <p style="text-align: start; font-size: 12px; margin: 0 0 15px 0">
              <span style="font-weight: 900">Further information<br /></span>
              Further information on the application of Classification codes,
              primarily aimed at inspectors but of possible interest to persons
              ordering condition reports, can be found in Electrical Safety
              First’s Best Practice Guide No 4 Electrical installation condition
              reporting: Classification Codes for domestic and similar
              electrical installations. The guide can be viewed or downloaded
              free of charge from www. electricalsafetyfirst.org.uk
            </p>
