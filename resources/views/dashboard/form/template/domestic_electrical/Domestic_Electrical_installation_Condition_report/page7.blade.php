              The purpose of periodic inspection is to determine, so far as is
              reasonably practicable, whether an electrical installation is in a
              satisfactory condition for continued service. This report provides
              an assessment of the condition of the electrical installation
              identified overleaf at the time it was inspected and tested,
              taking into account the stated extent of the installation and the
              limitations of the inspection and testing.<br />
              This report has been issued in accordance with the national
              standard for the safety of electrical installations, BS 7671: 2018
              – Requirements for Electrical Installations.<br />
              The report identifies any damage, deterioration, defects and/or
              conditions found by the inspector which may give rise to danger
              (see PART 6), together with any items for which improvement is
              recommended. If you were the person ordering this report, but not
              the user of the installation, you should pass this report, or a
              full copy of it including these notes, the schedules and
              additional pages (if any), immediately to the user.<br />
              This report should be retained in a safe place and shown to any
              person inspecting or undertaking further work on the electrical
              installation in the future. If you later vacate the property, this
              report will provide the new user with an assessment of the
              condition of the electrical installation at the time the periodic
              inspection was carried out.<br />
              Where the installation incorporates a residual current device
              (RCD) there should be a notice at or near the device stating that
              it should be tested every six months. For safety reasons it is
              important that this instruction is followed.<br />
              <span style="font-weight: 900">
                For safety reasons, the electrical installation should be
                re-inspected at appropriate intervals by a skilled person or
                persons, competent in such work.<br />
              </span>
              The recommended date by which the next inspection should be
              carried out is stated in PART 5 of this report. There should also
              be a notice at or near the main switchboard or distribution
              board/consumer unit indicating when the next inspection of the
              installation is due.
            </p>
            <p style="text-align: start; font-size: 12px; margin: 0">
              You should have received the report marked ‘Original’ and the
              contractor should have retained the report marked ‘Duplicate’.<br />
              This report form is intended to be issued only for the purpose of
              reporting on the condition of an existing electrical installation
              and must not be issued to certify new electrical installation work
              including the replacement of a distribution board or consumer
              unit.<br />
              The report consists of at least six numbered pages. Additional
              numbered pages may have been provided to permit further relevant
              information relating to the installation to be recorded. For
              installations having more than one distribution board or more
              circuits than can be recorded on PART 12, one or more additional
              Schedules of Circuit Details and Test Results should form part of
              the report. The report is invalid if any of the schedules
              identified in PART 10 are missing.
            </p>
