              PART 7 (Details and limitations) should identify fully the extent
              of the installation covered by this report and any limitations on
              the inspection and testing. The inspector should have agreed these
              aspects with the person ordering the report and with other
              interested parties (licensing authority, insurance company,
              mortgage
